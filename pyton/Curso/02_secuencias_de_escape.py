# \n : New line
# \"
txt = "We are the so-called \"Vikings\" from the north."
print(txt)
# \\ : Backslash
# \r :
txt = "Hello\rWorld!"
print(txt)
# \t
txt = "Hello\tWorld!"
print(txt)
# \b
txt = "Hello \bWorld!"
print(txt)

txt = "\110\145\154\154\157"
print(txt)

txt = "\x48\x65\x6c\x6c\x6f"
print(txt)
